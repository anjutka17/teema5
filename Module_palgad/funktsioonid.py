# 1. list() — превращает строку в список символов.

# 2. print() — выводит текст или данные на экран.

# 3. f”” (f-string) — форматирование строк с подстановкой переменных.

# 4. len() — возвращает количество элементов (длину) строки или списка.

# 5. range() — генерирует последовательность чисел.

# 6. input() — ввод данных с клавиатуры.

# 7. append() — добавляет элемент в конец списка.

# 8. for … in — цикл для перебора элементов.

# 9. extend() — добавляет в список все элементы другого списка.

# 10. insert() — вставляет элемент в указанную позицию списка.

# 11. remove() — удаляет первый найденный элемент по значению.

# 12. pop() — удаляет элемент по индексу (или последний, если индекс не указан).

# 13. index() — возвращает индекс первого найденного элемента.

# 14. count() — считает количество вхождений элемента в список.

# 15. sort() — сортирует список.

# 16. reverse() — разворачивает список в обратном порядке.

# 17. copy() — создает копию списка.

# 18. clear() — очищает список.

# 19. from string import * — импортирует готовые строки с буквами, цифрами и символами.

# 20. lower() — переводит строку в нижний регистр.

# 21. isdigit() — проверяет, состоит ли строка только из цифр.

# 22. get() — возвращает значение по ключу в словаре.

# 23. min() — находит минимальное значение.

# 24. max() — находит максимальное значение.

# 25. sum() — считает сумму всех чисел в списке.

# 26. int() — превращает строку в целое число.

# 27. float() — превращает строку в дробное число (с плавающей точкой).

# 28. try … except — обработка ошибок (если возникает ошибка — выполняется блок except).

# 29. sqrt() — извлекает квадратный корень.

# 30. round(число, знаков) — округляет число до указанного количества знаков после запятой.

# 31. pi — константа числа Пи (3.14159).

# 32. and — логический оператор “и”.

# 33. if … else — условный оператор (если / иначе).

# 34. ** — возведение в степень (например, a ** 2).

# 35. *” “num — умножение строки на число (повторяет строку num раз).

# * — оператор умножения.

# / — оператор деления.

# + — оператор сложения.

# - — оператор вычитания.

# % — остаток от деления.

# == — проверка на равенство.

# != — проверка на неравенство.

# < — меньше.

# <= — меньше или равно.

# > — больше.

# >= — больше или равно.

